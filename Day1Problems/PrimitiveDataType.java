package com.bridgelabz.Day1Problems;

public class PrimitiveDataType {
	static byte x;
	static short y;
	static int z;
	static long h;
	static float i;
	static double j;
	static boolean k;
	static char l;
	
	public static void main(String[] args) {
		System.out.println("display the default value of all primitive data types");
		System.out.println("byte ="+ x);
		System.out.println("short ="+ y);
		System.out.println("int ="+ z);
		System.out.println("long ="+ h);
		System.out.println("float ="+ i);
		System.out.println("double ="+ j);
		System.out.println("boolean ="+ k);
		System.out.println("char ="+ l);

	}

}
